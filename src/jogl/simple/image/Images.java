    // get image method
    public BufferedImage getImage(int[][] input) {
        
        BufferedImage output = new BufferedImage(xSize, ySize, BufferedImage.TYPE_INT_RGB);
        
        for (int x = 0; x < xSize; x++) {
            for (int y = 0; y < ySize; y++) {
                int color = input[x][y];
                
                // assign colors based on numbers in image
                switch (color) {
                    case 0: 
                        color = Color.BLACK.getRGB();
                        break;
                    case 1: 
                        color = Color.MAGENTA.getRGB();
                        break;                        
                    case 2: 
                        color = Color.RED.getRGB();
                        break;                        
                    case 3: 
                        color = Color.DARK_GRAY.getRGB();
                        break;                        
                    case 4:
                        color = Color.WHITE.getRGB();
                        break;                        
                    case 5:
                        color = Color.BLUE.getRGB();
                        break;                        
                    case 6:
                        color = Color.YELLOW.getRGB();
                        break;
                    default:
                        color = Color.BLACK.getRGB();
                        break;
                } // end switch
                output.setRGB(x, y, color);
            } // end y loop
        } // end x loop
        return output;
    } // end get image method
    
